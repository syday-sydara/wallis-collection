  "SESSION_MISSING",
  "SESSION_MALFORMED",
  "SESSION_SIGNATURE_INVALID",
  "SESSION_DECODE_FAILED",
  "SESSION_EXPIRED",
  "SESSION_HIGH_RISK",
  "PERMISSION_CHECK",
  "PERMISSION_DENIED",
  "RATE_LIMIT_CHECK",
