// lib/events/types.ts

/* -------------------------------------------------- */
/* Base Event Envelope (v3 Unified)                    */
/* -------------------------------------------------- */
export type BaseEvent = {
  id?: string; // generated by event bus
  timestamp?: string; // ISO string
  requestId?: string | null;
  source?: string | null; // "api" | "auth" | "system" | "job"
  category?: string | null; // "auth" | "rate_limit" | "fraud" | "system" | ...
  severity?: "low" | "medium" | "high";
  userId?: string | null;
  ip?: string | null;
  userAgent?: string | null;
  encryptedMetadata?: boolean; // whether metadata was encrypted
  metadata?: Record<string, any>;
};

